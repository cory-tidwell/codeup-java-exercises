public class StringExercise {
    public static void main(String[] args) {
        String output = "We don't need no education";
        System.out.println(output);
        output = "We don't need no thought control\n";
        System.out.println(output);
        output = "Check \"this\" out, \"s inside of \"s!\n";
        System.out.println(output);
        output = "In windows, the main drive is usually C:\\\n";
        System.out.println(output);
        output = "I can do backslashes \\, double backslashes \\\\, \n and the amazing triple backslash \\\\\\!";
        System.out.println(output);

    }
}
